    public static String readFile(String path) {

        createNewFile(path);



        StringBuilder sb = new StringBuilder();

        FileReader fr = null;

        try {

            fr = new FileReader(new File(path));



            char[] buff = new char[1024];

            int length = 0;



            while ((length = fr.read(buff)) > 0) {

                sb.append(new String(buff, 0, length));

            }

        } catch (IOException e) {

            e.printStackTrace();

        } finally {

            if (fr != null) {

                try {

                    fr.close();

                } catch (Exception e) {

                    e.printStackTrace();

                }

            }

        }



        return sb.toString();

    }



